def inverse(ch):
  return ch[::-1]     #retourner les éléments de la chaîne de caractères mais en ordre inversé

ch=input("donnez votre chaîne de caractères : ")          #demender à l'utilisateur d'entrer une chaîne de caractères et gardes sa valeur dans "ch"
print("voilà votre chaîne de caractères inversé :",inverse(ch))   #afficher la chaîne de caractères inversé



